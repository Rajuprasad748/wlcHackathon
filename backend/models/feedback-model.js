const mongoose = require('mongoose')
const { Schema } = mongoose;

const feedbackSchema = new Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports =  mongoose.model("feedback", feedbackSchema);
